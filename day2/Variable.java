package day2;

public class Variable {
	
	public static void main(String[] args) {
		
		int age = 30;
		System.out.println("Value of age = "+age);
		
		long num = 10_000_000_000l;
		System.out.println("Value of long num = "+num);
		
		char alp = 'R';
		System.out.println("Value of alphabet = "+alp);
		
		float pi = 3.14F;
		System.out.println("Value of float = "+pi);
		
		double d = 85.58965;
		System.out.println("Value of double "+d);
		
		boolean isPrime = false;
		System.out.println("Prime status = "+isPrime);
	}

}
